package abc.com.google.mytransportapi;

public class Transportdetails {

    private String id;
    private String name;
    private String corx;
    private  String cory;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCorx() {
        return corx;
    }

    public void setCorx(String corx) {
        this.corx = corx;
    }

    public String getCory() {
        return cory;
    }

    public void setCory(String cory) {
        this.cory = cory;
    }
}
